    const getFavStore = () => JSON.parse(localStorage.getItem('cocoshef_favorites')) || {};
    function getFavorites() {
        const u = getUser();
        if (!u) return [];
        return getFavStore()[u.login] || [];
    }
    const isFavorite = (id) => getFavorites().some(x => String(x) === String(id));
    function toggleFavorite(id) {
        const u = getUser();
        if (!u) return;
        const store = getFavStore();
        const list = store[u.login] || [];
        const idx = list.findIndex(x => String(x) === String(id));
        if (idx === -1) list.push(id); else list.splice(idx, 1);
        store[u.login] = list;
        try { localStorage.setItem('cocoshef_favorites', JSON.stringify(store)); } catch (e) {}
    }
