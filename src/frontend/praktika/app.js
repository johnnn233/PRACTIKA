    // Все комментарии текущего пользователя по всем рецептам и статьям
    function getUserComments(login) {
        const store = getComments();
        const result = [];
        Object.keys(store).forEach(entityId => {
            (store[entityId] || []).forEach(c => {
                if (c.login === login) result.push({ entityId, text: c.text, date: c.date });
            });
        });
        return result;
    }

    function findEntityTitle(entityId) {
        const r = getRecipes().find(x => String(x.id) === String(entityId));
        if (r) return { title: r.title, type: 'recipe' };
        const a = ARTICLES.find(x => x.id === entityId);
        if (a) return { title: a.title, type: 'article' };
        return null;
    }

