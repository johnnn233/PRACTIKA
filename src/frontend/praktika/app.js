    let randomOrder = [];   // перемешанный список рецептов
    let randomIndex = 0;

    function shuffle(arr) {
        const a = [...arr];
        for (let i = a.length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [a[i], a[j]] = [a[j], a[i]];
        }
        return a;
    }

    function renderRandom() {
        if (!randomContainer) return;
        randomOrder = shuffle(getRecipes());
        randomIndex = 0;
        showRandomAt(0);
    }

