    function renderFeed() {
        if (!featuredRecipeContainer || !recipesGrid) return;
        let list = recipesForPage();

        // Поиск
        const q = searchInput ? searchInput.value.trim().toLowerCase() : '';
        if (q) {
            list = list.filter(r =>
                r.title.toLowerCase().includes(q) ||
                r.description.toLowerCase().includes(q) ||
                (r.ingredients || []).some(i => i.toLowerCase().includes(q))
            );
        }

        list = sortRecipes(list);

        if (list.length === 0) {
            featuredRecipeContainer.innerHTML = '';
            recipesGrid.innerHTML = `<p class="no-results">По вашему запросу ничего не найдено.</p>`;
            return;
        }

        // При поиске показываем все результаты сеткой, без выделенного блока
        if (q) {
            featuredRecipeContainer.innerHTML = '';
            recipesGrid.innerHTML = list.map(recipeCard).join('');
        } else {
            renderFeatured(list[0]);
            const rest = list.slice(1);
            if (!rest.length) {
                recipesGrid.innerHTML = `<p class="no-results">Здесь пока только одно главное блюдо. Создайте своё!</p>`;
            } else {
                let html = adBannerHtml(pickAd(0), true);   // баннер во всю ширину сверху
                rest.forEach((rec, i) => {
                    html += recipeCard(rec);
                    if ((i + 1) % 6 === 0) html += adCardHtml(pickAd(i)); // реклама каждые 6 карточек
                });
                html += adStripHtml(pickAd(3));             // полоса-реклама снизу
                recipesGrid.innerHTML = html;
            }
        }

        bindEntity(featuredRecipeContainer);
        bindEntity(recipesGrid);
    }

